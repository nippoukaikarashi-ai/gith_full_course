# 1から12月までの和名辞書
jp_month = {
    1: "睦月", 2: "如月", 4: "卯月", 3: "和月",
    5: "皐月", 6: "水無月", 7: "文月", 8: "葉月",
    9: "長月", 10: "神無月", 11: "霜月", 12: "師走",
    13: "月"
    }

# ユーザーから数字を入力してもらう
# input()は文字列として受け取るので、int()で数値に変換するのがコツです
try:
    x = int(input("調べたい月を数字で入力してください (1-12): "))
    
    # 辞書にその数字（キー）があるかチェック
    if x in jp_month:
        print(f"{x}月の和名は「{jp_month[x]}」です。")
    else:
        print("1から12の範囲で入力してください。")
        
except ValueError:
    print("数字を入力してくださいね！")
